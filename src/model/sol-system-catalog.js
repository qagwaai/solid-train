// ---------- Moons ----------
//
// Orbits are parent-relative (around the parent planet/dwarf body).
// For brevity, mean anomaly at epoch is approximate or zero where the moon's
// J2000 phase is not commonly tabulated. This is acceptable because gameplay
// uses the elements primarily for visualization, not navigation accuracy.

function makeMoon(entry) {
  return {
    id: entry.id,
    displayName: entry.displayName,
    bodyType: 'moon',
    parentBodyId: entry.parentBodyId,
    orbit: {
      semiMajorAxisKm: entry.semiMajorAxisKm,
      eccentricity: entry.eccentricity ?? 0,
      inclinationDeg: entry.inclinationDeg ?? 0,
      longitudeOfAscendingNodeDeg: entry.longitudeOfAscendingNodeDeg ?? 0,
      argumentOfPeriapsisDeg: entry.argumentOfPeriapsisDeg ?? 0,
      meanAnomalyAtEpochDeg: entry.meanAnomalyAtEpochDeg ?? 0,
      orbitalPeriodSec: days(entry.orbitalPeriodDays),
      epoch: J2000_EPOCH,
    },
    physical: {
      massKg: entry.massKg ?? null,
      meanRadiusKm: entry.meanRadiusKm,
      rotationPeriodSec:
        entry.tidallyLocked === false
          ? (entry.rotationPeriodSec ?? null)
          : days(entry.orbitalPeriodDays),
      axialTiltDeg: entry.axialTiltDeg ?? null,
      surfaceGravityMps2: entry.surfaceGravityMps2 ?? null,
      meanTemperatureK: entry.meanTemperatureK ?? null,
      compositionTags: entry.compositionTags || ['rock', 'ice'],
    },
    atmosphere: entry.atmosphere || null,
    discovery: entry.discovery || {
      discoveredBy: null,
      discoveredYear: null,
      discoveryNotes: null,
    },
    magnitudes: entry.magnitudes || null,
  };
}

