'use strict';

/**
 * Static catalog of well-known Sol system bodies used to seed the celestial body
 * collection. Orbital elements are heliocentric for planets/dwarf planets/asteroids/TNOs
 * (referenced to the J2000.0 ecliptic) and parent-relative for moons.
 *
 * Sources (game-grade approximations, not flight-grade ephemerides):
 *   - NASA planetary fact sheets (J2000.0 mean orbital elements)
 *   - JPL Horizons for major moons
 *   - IAU minor body data for dwarf planets, asteroids, and TNOs
 *
 * Conventions:
 *   - All angular elements in degrees.
 *   - semiMajorAxisKm: km. For planets/dwarfs/asteroids/TNOs heliocentric.
 *     For moons, distance from parent body.
 *   - meanAnomalyAtEpochDeg: M at the listed epoch.
 *   - epoch: J2000.0 = 2000-01-01T12:00:00.000Z.
 *   - massKg / meanRadiusKm rounded to a sensible significant-figure count.
 *   - When real-world data is uncertain or irrelevant for gameplay (e.g.
 *     small irregular moons), values are reasonable approximations.
 */

const J2000_EPOCH = '2000-01-01T12:00:00.000Z';
const SECONDS_PER_DAY = 86400;
const AU_KM = 149_597_870.7;

function days(value) {
  return value * SECONDS_PER_DAY;
}

function au(value) {
  return value * AU_KM;
}

